const initialState = {
  userInfo: userData,
  fetching: false,
  fetched: false,
  error_msg: '',

  userAlbum: [],
  albumFetching: false,
  albumFetched: false,
  albumError_msg: '',

  albumPhotos: [],
  photosFetching: false,
  photosFetched: false,
  photosError_msg: ''
};

export function userReducer(state=initialState, action)
{
  switch (action.type)
  {
    case FETCH_USER_START:
      return {...state, fetching: true};
    case FETCHED_USER:
      return {...state,
        userInfo: action.payload,
        fetched: true,
        fetching: false,
        error_msg: ''
      };
    case FETCH_USER_ERROR:
      return {...state, error_msg: action.payload};
    case RESET_USER:
      return {...initialState};

    case FETCH_USER_ALBUM_START:
      return {...state, albumFetching: true, userAlbum:[]};
    case FETCHED_USER_ALBUM:
      return {...state,
        userAlbum: action.payload,
        albumFetched: true,
        albumFetching: false,
        albumError_msg: '',
        albumPhotos: [],
        photosFetched: false,
      };
    case FETCH_USER_ALBUM_ERROR:
      return {...state, albumError_msg: action.payload};

    case FETCH_USER_ALBUM_PHOTOS_START:
      return {...state, photosFetching: true};
    case FETCHED_USER_ALBUM_PHOTOS:
      return {...state,
        albumPhotos: action.payload,
        photosFetched: true,
        photosFetching: false,
        photosError_msg: '',
      };
    case FETCH_USER_ALBUM_PHOTOS_ERROR:
      return {...state, photosError_msg: action.payload};

    default:
      return state;
  }
}